    public void bases() {
        sql = "SHOW DATABASES";
        jComboBox1.removeAllItems();

        try {
            stmt = con.prepareStatement(sql);
            tabla = stmt.executeQuery();
            while (tabla.next()) {
                jComboBox1.addItem(tabla.getString(1));
            }
        } catch (SQLException e) {
            DesktopNotify.showDesktopMessage("Error", e.getMessage(), 8, 8000);
        }
    }

