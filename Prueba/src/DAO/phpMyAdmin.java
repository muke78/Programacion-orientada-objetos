    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        TXT_AREA2.setText("");
        mytabla = jComboBox1.getItemAt(jComboBox1.getSelectedIndex());
        sql = "use " + mytabla;
        mytabla = jComboBox2.getItemAt(jComboBox2.getSelectedIndex());

        sql2 = TXT_AREA.getText();

        String p = sql2;
        String arr[] = p.split(" ");

        String p1 = arr[0];
        System.out.println(p1);

        if (p1.equals("select")||p1.equals("SELECT")||p1.equals("desc")||p1.equals("DESC")||p1.equals("show")||p1.equals("SHOW")) {
            try {
                sql2 = TXT_AREA.getText();
                stmt = con.prepareStatement(sql2);
                tabla = stmt.executeQuery();
                tabla = con.createStatement().executeQuery(sql2);
                int len = tabla.getMetaData().getColumnCount();
                System.out.println(len);
                while (tabla.next()) {
                    for (int i = 1; i <= len; i++) {
                        String t1 = tabla.getString(i);
                        if (i <= len) {
                            TXT_AREA2.append(t1 + ", ");
                        }
                    }
                    TXT_AREA2.append("\n");
                }
            } catch (SQLException e) {
                DesktopNotify.showDesktopMessage("Error", e.getMessage(), 8, 8000);
            }
        } else {
            try {
                sql2 = TXT_AREA.getText();
                stmt2 = con.prepareStatement(sql2);
                sw = stmt2.executeUpdate();
                if (sw != 0) {
                   TXT_AREA2.append("Comando ejecutado con exito");
                    TXT_AREA.setText(" ");
               }
            } catch (SQLException e) {
               TXT_AREA2.append(e.getMessage());
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

